import Foundation

class Colors {
    static func appColor() -> UIColor {
        return UIColor(hexCode: 0x14213D)
    }
    
    static func acceptColor() -> UIColor {
        return UIColor(hexCode: 0x008000)
    }
    
    static func dismissColor() -> UIColor {
        return UIColor(hexCode: 0x800000)
    }
}

